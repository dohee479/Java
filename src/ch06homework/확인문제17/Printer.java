package ch06homework.확인문제17;

public class Printer {
	
	static void println(int n) {
		System.out.println(n);
	}
	
	static void println(String n) {
		System.out.println(n);
	}
	
	static void println(boolean n) {
		System.out.println(n);
	}
	
	static void println(double n) {
		System.out.println(n);
	}
}
